package com.biz.rev;

public class Var_04 {

	public static void main(String[] args) {
		
		int num = 3 * 4 ;
		
		// 위의 변수 num을 무시하고
		// 아래의 수식을 num에 저장
		num = 4/2;

		String s = "KOREA";
		
		// 앞에서 선언된 변수 s에
		// 3가지 문자열을 연결하여 저장하라
		s = "Republic " + "of " + "Korea";
		// s에 저장된 값?
		// Republic of Korea
		
		s = "30 + 40";
		// s = 30 + 40
		
	}
}
