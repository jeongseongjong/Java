package com.biz.rev;

public class Var_01 {

	public static void main(String[] args) {

		// 30 + 40 = ?
		System.out.println(30 + 40);
		/*
		 * 컴퓨터가 기억했다가 연산을 수행하고 콘솔에 보여줌
		 */

		/*
		 * 30과 40을 원하는곳에 저장하고 계산후에 콘솔에 표시하고싶다.
		 * 
		 * 변수를 이용 코딩
		 */

		int num1 = 30;
		int num2 = 40;

		// 정수형 변수 sum을 선언하라

		int sum;
		sum = num1 + num2;
		
		System.out.println(sum);
	}
}
