package com.biz.Var;

public class Var_01 {

	/*
	 * 클래스에 main() 메서드가 잇으면
	 * main() 메서드에 작성된 코드가 직접 실행된다.
	 */
	public static void main(String[] args) {
		
		// 숫자변수에 0을 저장하는것 == clear
		// 변수 어떤값을 저장해야할지 결정하지 않았을 때
		// 혹시 모를 변술르 읽을 때 발생할 오류를 방지하는
		// 목적이 있다.
		int num = 0 ;
		float f = 0 ;
		long l = 0;
		double d = 0;
		
		// 문자열 변수에 ""을 저장하는것
		// 이것또한 clear
		String str = ""; 
		
		boolean b = true;
		boolean b1 = false;
		
	}
}
